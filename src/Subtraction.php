<?php
/**
 * \EasyMath\Subtraction Class
 */

namespace EasyMath;

/**
 * Class containing subtraction methods
 */
class Subtraction
{
    /**
     * Subtract 2 numbers
     * @param float $x
     * @param float $y
     * @return float
     */
    public function subtract($x, $y)
    {
        return $x - $y;
    }
}